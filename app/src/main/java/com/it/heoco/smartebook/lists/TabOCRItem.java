public class TabOCRItem {
    private int icon;
    private String title;

    public TabOCRItem(int icon, String title) {
        this.icon = icon;
        this.title = title;
    }

    public int getIcon() {
        return icon;
    }

    public String getTitle() {
        return title;
    }
}
